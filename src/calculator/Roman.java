public class Roman {
    private static final String[] ROMAN_NUMBERS = {"zero", "I", "II", "III", "IV", "V", "VI", "VII", "VIII", "IX", "X",
            "XI", "XII", "XIII", "XIV", "XV", "XVI", "XVII", "XVIII", "XIX", "XX",
            "XXI", "XXII", "XXIII", "XXIV", "XXV", "XXVI", "XXVII", "XXVIII", "XXIX", "XXX",
            "XXXI", "XXXII", "XXXIII", "XXXIV", "XXXV", "XXXVI", "XXXVII", "XXXVIII", "XXXIX", "XL",
            "XLI", "XLII", "XLIII", "XLIV", "XLV", "XLVI", "XLVII", "XLVIII", "XLIX", "L",
            "LI", "LII", "LIII", "LIV", "LV", "LVI", "LVII", "LVIII", "LIX", "LX",
            "LXI", "LXII", "LXIII", "LXIV", "LXV", "LXVI", "LXVII", "LXVIII", "LXIX", "LXX",
            "LXXI", "LXXII", "LXXIII", "LXXIV", "LXXV", "LXXVI", "LXXVII", "LXXVIII", "LXXIX", "LXXX",
            "LXXXI", "LXXXII", "LXXXIII", "LXXXIV", "LXXXV", "LXXXVI", "LXXXVII", "LXXXVIII", "LXXXIX", "XC",
            "XCI", "XCII", "XCIII", "XCIV", "XCV", "XCVI", "XCVII", "XCVIII", "XCIX", "C"};

    public static int romToArabian(String romanNumber) throws Exception {
        String inpUp = romanNumber.toUpperCase();
        for (int i = 0; i < ROMAN_NUMBERS.length; i++) {
            if (inpUp.equals(ROMAN_NUMBERS[i])) {
                return i;
            }
        }
        throw new Exception("Обе переменные должны быть либо римскими, либо арабскими!");
    }

    public static String arabToRoman(int arabNumber) {
        if (arabNumber <= 0) {
            throw new IllegalArgumentException("Результат операций римских чисел не может быть меньше или равен нулю!");
        }
        return ROMAN_NUMBERS[arabNumber];
    }
}
